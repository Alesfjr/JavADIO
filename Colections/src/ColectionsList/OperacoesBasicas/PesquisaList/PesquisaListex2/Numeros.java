package ColectionsList.OperacoesBasicas.PesquisaList.PesquisaListex2;

public class Numeros {
    private int numero;

    public int getNumero() {
        return numero;
    }

    public Numeros(int numero) {
        this.numero = numero;
    }

    @Override
    public String toString() {
        return "Numeros{" +
                "numero=" + numero +
                '}';
    }
}
